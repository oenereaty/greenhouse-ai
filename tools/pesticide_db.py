def detect_diseases(text: str) -> list[str]:
    """텍스트에서 병해충 키워드 감지 → 일치하는 disease 이름 목록 반환."""
    found = [d for d in DISEASE_MAP if d in text]
    if not found and any(kw in text for kw in _SPRAY_TRIGGERS):
        return list(DISEASE_MAP.keys())
    return found


def detect_all_tags(text: str) -> list[str]:
    """텍스트에서 달력 뱃지용 태그 전체 감지 (작업+병해+충해 포함)."""
    return [kw for kw in _TAG_KEYWORDS if kw in text]


def get_autocomplete(prefix: str, max_results: int = 6) -> list[str]:
    """prefix로 시작하는 자동완성 후보 (최대 max_results개)."""
    if not prefix or len(prefix) < 1:
        return []
    return [t for t in AUTOCOMPLETE_TERMS if t.startswith(prefix)][:max_results]
